import math

# Operasi akar dan pangkat
print("Akar kuadrat 16:", math.sqrt(16)) # 4.0
print("Pangkat 2 pangkat 3:", math.pow(2,3)) # 8.0
print("Faktorial 5:", math.factorial(5)) # 120
print("Nilai PI:", math.pi) # 3.141592653589793
print("Nilai e:", math.e) # 2.718281828459045

# Pembulatan
print("Pembulatan ke atas 4.5:", math.ceil(4.5)) # 5
print("Pembulatan ke bawah 4.5:", math.floor(4.5)) # 4
print("Pembulatan terdekat 4.5:", round(4.5)) # 4

# Operasi trigonometri
print("Sinus 90 derajat:", math.sin(math.radians(90))) # 1.0
print("Cosinus 60 derajat:", math.cos(math.radians(60))) # 0.5
print("Tangen 45 derajat:", math.tan(math.radians(45))) # 1.0

# Logaritma
print("Logaritma basis 10 dari 100:", math.log10(100)) # 2.0
print("Logaritma basis e dari 10:", math.log(10)) # 2.302585092994046

# Fungsi lainnya
print("Nilai absolut -5:", math.fabs(-5)) # 5.0
print("Nilai maksimum 5 dan 10:", math.fmax(5, 10)) # 10
print("Nilai minimum 5 dan 10:", math.fmin(5, 10)) # 5
